
<h1 class="col-xs-12 text-center">About</h1>

		<p class="col-xs-12">
		Get My Song Reviewed (getmysongreviewed.com) is a website created for songwriters, musicians, and the audio recording enthusiast. It allows these artists to get reviews of their songs 
		by doing reviews of other songs from other artists. There is no charge, the service is completely free. An artist will get a review back for every review the artist completes.
		</p>
		
		<p class="col-xs-12">
		The reviews are anonymous. The song being reviewed and the reviewer are unknown to the other party. Get My Song Reviewed (getmysongreviewed.com) feels that this is the best way to get the most 
		honest reviews. 
		</p>
		
		<p class="col-xs-12">
		The reviews are simple. Multiple choice questions that an artist can complete as the song plays. There are no typed sections in the reviews. An artist can do up to 20 reviews 
		per song that they upload, after which, the artist can choose a new song (there is no reason it couldn't be the same song). An artist can upload only one song at a time.
		</p>
		
		<p class="col-xs-12">
		There are many sites available to an artist for networking, getting fans, posting their gigs etc. but not so many for getting honest feedback of their music.  Where honest 
		feedback does exist it is often expensive, or requires long typed out passages that can make doing reviews seem more like a job than relaxing and listening to music.
		</p>
		
		
		<p class="col-xs-12">
		Get My Song Reviewed (getmysongreviewed.com) hopes that this website will be useful, and be a tool that is simple to use and helps an artist create better songs.
		</p>
		
		
